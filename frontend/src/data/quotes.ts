/**
 * 根据日期稳定地从名言列表中选取一条（同一天得到同一条），
 * 可选 `offset` 用于"下一条/上一条"的翻动。
 */
export function getDailyQuote(offset = 0, date: Date = new Date()): InvestmentQuote {
  // 以"天"为稳定种子（本地时区），避免同一天频繁跳动
  const y = date.getFullYear();
  const m = date.getMonth() + 1;
  const d = date.getDate();
  const seed = y * 10000 + m * 100 + d;
  const n = INVESTMENT_QUOTES.length;
  const idx = (((seed + offset) % n) + n) % n;
  return INVESTMENT_QUOTES[idx];
}
